async function handlePrefs(request, env) {
  const u = new URL(request.url);
  const uid = u.searchParams.get("uid") || "anon";
  if (request.method === "GET") {
    const saved = env.HISTORY ? await env.HISTORY.get("prefs:"+uid,"json") : null;
    const answers = (saved && saved.answers) || {};
    return json({ quiz: QUIZ, profiles: Object.keys(PROFILES), saved,
      weights: weightsFromAnswers(answers, PROFILES[(saved&&saved.profile)||"balanced"]) });
  }
  const body = await request.json().catch(()=>({}));
  const rec = { profile: body.profile || "balanced", answers: body.answers || {}, savedAt: new Date().toISOString() };
  if (env.HISTORY) await env.HISTORY.put("prefs:"+uid, JSON.stringify(rec));
  return json({ ok:true, saved:rec, weights: weightsFromAnswers(rec.answers, PROFILES[rec.profile]) });
}

async function handleGround(request, env) {
  const u = new URL(request.url);
  const uid = u.searchParams.get("uid") || "anon";
  if (request.method === "GET") return json({ ground: await getGround(env, uid) });
  const body = await request.json().catch(()=>({}));
  const cur = await getGround(env, uid);
  if (body.code) {
    if (body.remove) delete cur[body.code.toUpperCase()];
    else cur[body.code.toUpperCase()] = {
      costHKD: Math.max(0, parseInt(body.costHKD||0,10)),
      minutes: Math.max(0, parseInt(body.minutes||0,10)),
      label: (body.label||"").slice(0,60),
    };
  }
  if (env.HISTORY) await env.HISTORY.put("ground:"+uid, JSON.stringify(cur));
  return json({ ok:true, ground:cur });
}

