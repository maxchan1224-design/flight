function agencyTier(name) {
  if (!name) return { tier: 3, label: "unknown agency" };
  for (const k of Object.keys(AGENCY_TIER)) {
    if (name.toLowerCase().includes(k.toLowerCase())) return { tier: AGENCY_TIER[k], label: name };
  }
  return { tier: 3, label: name };
}

