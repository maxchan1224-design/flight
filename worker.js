/* ================================================================
 * TRUE COST — arithmetic only. The LLM never touches this.
 * ================================================================ */

function trueCost(offer, w, ground, homeOrigin) {
  const parts = [];
  let total = offer.priceTotal;
  parts.push({ k: "fare", v: offer.priceTotal });

  let groundKnown = true;
  if (offer.origin !== homeOrigin) {
    const g = ground[offer.origin];
    if (g) {
      const c = g.costHKD * (offer.returnDate ? 2 : 1);
      const hrs = (g.minutes * (offer.returnDate ? 2 : 1)) / 60;
      total += c; parts.push({ k: "ground", v: c, detail: g.label });
      total += hrs * w.timeValue; parts.push({ k: "groundTime", v: Math.round(hrs * w.timeValue), hours: hrs });
    } else {
      groundKnown = false;
      parts.push({ k: "ground", v: null, unknown: true });
    }
  }

  if (offer.stops) {
    const p = offer.stops * w.stopPenalty;
    total += p; parts.push({ k: "stops", v: p, count: offer.stops });
  }

  const ch = offer.airlineCode && AIRLINE_SITES[offer.airlineCode] ? 0
    : agencyTier(offer.agency).tier * w.channelPremium;
  if (ch) { total += ch; parts.push({ k: "channel", v: ch }); }

  return { total: Math.round(total), parts, groundKnown };
}

function eligible(offer, w, ground, homeOrigin) {
  if (offer.stops != null && w.maxStops != null && offer.stops > w.maxStops)
    return { ok: false, why: "exceeds_max_stops" };
  if (offer.origin !== homeOrigin) {
    const g = ground[offer.origin];
    // An airport whose ground cost we do NOT know must never compete in the
    // main ranking. Otherwise ignorance looks like cheapness: an unpriced
    // transfer scores as free and unfairly beats an honestly-costed one.
    if (!g) return { ok: false, why: "ground_cost_unknown" };
    if ((g.minutes / 60) > w.maxGroundHours) return { ok: false, why: "exceeds_max_ground_time" };
  }
  return { ok: true };
}

/* ================================================================
 * OPPORTUNITY RADAR
 *
 * Answers "book or wait" from RECORDED history only.
 * It never predicts a future promotion — no data source knows when
 * an airline's next sale starts, and inventing one would be the
 * exact failure mode this product exists to avoid.
 * ================================================================ */

function percentileOf(value, arr) {
  if (!arr.length) return null;
  const below = arr.filter(x => x < value).length;
  return Math.round((below / arr.length) * 100);
}

function radar(currentPrice, history, signals) {
  const pts = (history && history.points ? history.points : []).filter(p => p.price != null);
  const prices = pts.map(p => p.price);

  const base = {
    signals: signals || [],
    signalNote: "promotion_signals_are_reports_not_forecasts",
  };

  if (prices.length < 10) {
    return { ...base,
      verdict: "insufficient_data",
      samples: prices.length, needed: 10,
      canPredict: false,
      reason: "not_enough_recorded_history",
    };
  }

  const sorted = [...prices].sort((a,b) => a-b);
  const min = sorted[0], max = sorted[sorted.length-1];
  const med = sorted[Math.floor(sorted.length/2)];
  const pct = percentileOf(currentPrice, prices);

  // Trend over the most recent third vs the oldest third
  const third = Math.max(2, Math.floor(pts.length/3));
  const early = pts.slice(0, third).map(p=>p.price);
  const late  = pts.slice(-third).map(p=>p.price);
  const avg = a => a.reduce((x,y)=>x+y,0)/a.length;
  const drift = Math.round(((avg(late) - avg(early)) / avg(early)) * 100);

  let verdict;
  if (pct <= 15) verdict = "book_now";
  else if (pct <= 35) verdict = "lean_book";
  else if (pct >= 75) verdict = "wait";
  else verdict = "neutral";

  return { ...base,
    verdict, canPredict: true,
    samples: prices.length,
    currentPrice, percentile: pct,
    lowestSeen: min, highestSeen: max, medianSeen: med,
    trendPct: drift,
    confidence: prices.length >= 45 ? "high" : prices.length >= 20 ? "medium" : "low",
    daysCovered: pts.length ? (pts[0].date + " → " + pts[pts.length-1].date) : null,
  };
}

