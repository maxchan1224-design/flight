function offerCard(o,best,cur){
 var el=document.createElement('div');el.className='card'+(best?' best':'');
 var bits=[];
 if(o.departDate)bits.push('<span>'+o.departDate+(o.returnDate?(' → '+o.returnDate):'')+'</span>');
 if(o.stops!=null)bits.push('<span>'+(o.stops===0?(lang==='zh'?'直航':'Direct'):o.stops+(lang==='zh'?' 轉':' stop'))+'</span>');
 if(o.airlineName)bits.push('<span>'+o.airlineName+'</span>');
 bits.push('<span class="tag tag-'+o.via+'">'+o.via.replace('_',' ')+'</span>');

 var work='';
 (o.costParts||[]).forEach(function(p){
  if(p.unknown){work+='<div class="unk">'+t(p.k)+': '+t('unknown')+'</div>';return}
  if(p.k==='fare'){work+='<div>'+t('fare')+': '+money(p.v,cur)+'</div>';return}
  work+='<div class="neg">+ '+t(p.k)+': '+money(p.v,cur)+(p.hours?(' ('+p.hours.toFixed(1)+'h)'):'')+'</div>'});
 work+='<div style="border-top:1px solid var(--line);margin-top:5px;padding-top:5px"><b>'+t('tc')+': '+money(o.trueCost,cur)+'</b></div>';

 var bk=(o.channels||[]).filter(function(c){return c.kind==='book'});
 var cp=(o.channels||[]).filter(function(c){return c.kind!=='book'});
 var ch='<div class="ch"><div class="chl">'+t('ch_book')+'</div><div class="chw">';
 bk.forEach(function(c){
  var nm=lang==='zh'?c.nameZh:c.nameEn;
  if(c.url)ch+='<a class="b book" href="'+c.url+'" target="_blank" rel="noopener">'+nm+'</a>';
  else ch+='<span class="b dis" title="'+t('ag_only')+'">'+nm+'</span>'});
 if(!bk.length)ch+='<span class="b dis">—</span>';
 ch+='</div><div class="chl" style="margin-top:9px">'+t('ch_cmp')+'</div><div class="chw">';
 cp.forEach(function(c){ch+='<a class="b cmp" href="'+c.url+'" target="_blank" rel="noopener">'+(lang==='zh'?c.nameZh:c.nameEn)+'</a>'});
 ch+='</div></div>';

 el.innerHTML='<div class="ctop"><div><div class="route">'+o.origin+'<span class="ar">→</span>'+o.destination+'</div>'+
  '<div class="meta">'+bits.join('')+'</div></div>'+
  '<div class="price"><div class="p">'+money(o.priceTotal,cur)+'</div><div class="tc">'+t('tc')+' '+money(o.trueCost,cur)+'</div></div></div>'+
  '<div class="work">'+work+'</div>'+ch;
 return el}

