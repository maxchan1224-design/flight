const DEAL_WORDS = ["error fare","mistake fare","flash sale","promo","promotion","sale","deal","discount","% off","fare drop"];

async function promoSignals(env, q, names) {
  const key = "signals:v2";
  if (env.HISTORY) { const c = await env.HISTORY.get(key,"json"); if (c) return filterSignals(c, q, names); }
  const subs = ["TravelDeals","awardtravel","flights"];
  const out = [];
  await Promise.all(subs.map(async s => {
    try {
      const r = await fetch("https://www.reddit.com/r/"+s+"/new.json?limit=25",
        { headers:{ "User-Agent":"flight-orchestrator/1.0 (personal)" }});
      if (!r.ok) return;
      const d = await r.json();
      for (const c of (d.data&&d.data.children)||[]) {
        out.push({ source:"r/"+s, title:c.data.title,
          url:"https://reddit.com"+c.data.permalink, ts:c.data.created_utc*1000 });
      }
    } catch(e) {}
  }));
  if (env.HISTORY) await env.HISTORY.put(key, JSON.stringify(out), { expirationTtl: 10800 });
  return filterSignals(out, q, names);
}

function filterSignals(list, q, names) {
  const dest = (q.destination||"").toLowerCase();
  const org = (q.origin||"").toLowerCase();
  const fp = s => s.toLowerCase().replace(/[^a-z0-9 ]/g,"").replace(/\s+/g," ").trim().slice(0,70);
  const seen = new Map();
  for (const x of list) {
    const t = x.title.toLowerCase();
    let score = 0;
    for (const w of DEAL_WORDS) if (t.includes(w)) score += 2;
    if (t.includes("error fare")||t.includes("mistake fare")) score += 5;
    if (dest && t.includes(dest)) score += 5;
    if (org && t.includes(org)) score += 4;
    if (score < 5) continue;
    const k = fp(x.title);
    const prev = seen.get(k);
    if (!prev) seen.set(k, { ...x, score, alsoSeenIn: [] });
    else if (prev.source !== x.source && !prev.alsoSeenIn.includes(x.source)) {
      prev.alsoSeenIn.push(x.source); prev.score += 1;
    }
  }
  return [...seen.values()].sort((a,b)=>b.score-a.score||b.ts-a.ts).slice(0,8);
}

