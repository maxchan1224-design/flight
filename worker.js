/**
 * Derive weights from quiz answers. Each time-question gives a bound:
 * "yes" → timeValue <= save/hours ;  "no" → timeValue >= save/hours
 */
function weightsFromAnswers(answers, base) {
  const w = { ...(base || PROFILES.balanced) };
  let lo = 0, hi = 2000, bounded = false;

  for (const q of QUIZ) {
    const a = answers[q.id];
    if (a === undefined) continue;
    if (q.kind === "time") {
      const rate = q.save / q.hours;
      if (a === true) { hi = Math.min(hi, rate); bounded = true; }
      else { lo = Math.max(lo, rate); bounded = true; }
    }
    if (q.kind === "stop") w.stopPenalty = a === true ? Math.min(w.stopPenalty, q.save) : Math.max(w.stopPenalty, q.save + 100);
    if (q.kind === "channel") w.channelPremium = a === true ? Math.min(w.channelPremium, q.save) : Math.max(w.channelPremium, q.save + 100);
  }

  if (bounded) {
    if (lo > hi) w.timeValue = Math.round((lo + hi) / 2);      // inconsistent answers → midpoint
    else if (hi < 2000 && lo > 0) w.timeValue = Math.round((lo + hi) / 2);
    else if (hi < 2000) w.timeValue = Math.round(hi * 0.75);
    else w.timeValue = Math.round(lo * 1.25);
  }
  w.derivedFrom = bounded ? "quiz" : "profile";
  w.bounds = bounded ? { lo, hi } : null;
  return w;
}

